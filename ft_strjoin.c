#include "libft.h"

char *ft_strjoin(char const *s1, char const *s2)
{
	char *join;
	size_t len1;
	size_t len2;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	if (!(join = malloc(len1 + len2 + 1)))
		return (NULL);
	join[0] = '\0';
	ft_strlcpy(join, s1, len1 + 1);
	ft_strlcat(join, s2, len1 + len2 + 1);
	return (join);
}
